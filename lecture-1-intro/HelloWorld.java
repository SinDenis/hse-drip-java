/**
 * Лекция 1: Hello World - Первая программа на Java
 *
 * Это самая простая программа на Java, которая выводит текст на экран.
 * Давайте разберём каждую часть этой программы.
 */

public class HelloWorld {

    public static void main(String[] args) {

        System.out.println("Hello, World!");

        System.out.print("Без перевода строки");
        System.out.println();

        System.out.printf("Форматированный вывод: %d + %d = %d%n", 2, 3, 5);
    }
}

/*
 * КАК СКОМПИЛИРОВАТЬ И ЗАПУСТИТЬ:
 *
 * Способ 1 (классический):
 *   1. javac HelloWorld.java   (компиляция -> создаётся HelloWorld.class)
 *   2. java HelloWorld         (запуск)
 *
 * Способ 2 (Java 11+):
 *   java HelloWorld.java       (компиляция и запуск одной командой)
 *
 * ЧТО ПРОИСХОДИТ ПРИ КОМПИЛЯЦИИ:
 * 1. javac читает исходный код (.java файл)
 * 2. Проверяет синтаксис и типы
 * 3. Генерирует байт-код (.class файл) - промежуточный код для JVM
 *
 * ЧТО ПРОИСХОДИТ ПРИ ЗАПУСКЕ:
 * 1. JVM загружает .class файл
 * 2. Верифицирует байт-код (проверка безопасности)
 * 3. JIT (Just-In-Time) компилятор преобразует байт-код в машинный код
 * 4. Выполняется метод main
 */
